const DICTS: Record<Lang, Record<string, string>> = { tr: TR, en: EN };

interface LangContextType { lang: Lang; setLang: (l: Lang) => void; t: (key: string) => string; }
const LangContext = createContext<LangContextType>({ lang: "tr", setLang: () => {}, t: (k) => k });

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("tr");

  useEffect(() => {
    const stored = localStorage.getItem("tradet_lang") as Lang | null;
    if (stored === "tr" || stored === "en") setLangState(stored);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("tradet_lang", l);
  };

  const t = (key: string) => DICTS[lang][key] ?? DICTS["en"][key] ?? key;

  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
}

export function useLang() { return useContext(LangContext); }
export function useT() { return useContext(LangContext).t; }
